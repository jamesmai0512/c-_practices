//mode of the increment and deecrement operators.
#include<iostream>
using namespace std;
int main()
{
	int num = 4;
	 cout << num <<" ";
	 cout << num++ << " ";
	 cout << num <<" ";
	 cout << ++num << " \n\n";
	 	
		  cout << num <<" ";
	 cout << num-- << " ";
	 cout << num <<" ";
	 cout << --num << " \n\n";
	 
	 return 0;
}
